    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            if (txtTanggal.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Tanggal belum diisi");
                txtTanggal.requestFocus();
            } else if (txtJam.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Jam belum diisi");
                txtJam.requestFocus();
            } else if (txtTopik.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Topik belum diisi");
                txtTopik.requestFocus();
            // =================================================================
            // PERBAIKAN: MENAMBAHKAN VALIDASI UNTUK 'Isi' (karena NOT NULL)
            // =================================================================
            } else if (txtIsi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Isi belum diisi");
                txtIsi.requestFocus();
            } else {
                // Menggunakan simpanAgenda02 (PreparedStatement)
                objekKu.simpanAgenda02(
                        txtTanggal.getText(),
                        txtJam.getText(),
                        txtTopik.getText(),
                        txtIsi.getText(),
                        txtGambar.getText()
                );
                // Refresh data tabel
                objekKu.tampilDataAgenda(tableAgenda, "select * from agenda");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID Agenda belum diisi (Pilih data dari tabel)");
                txtId.requestFocus();
            } else {
                // Menggunakan ubahAgenda02 (PreparedStatement)
                objekKu.ubahAgenda02(
                        txtId.getText(),
                        txtTanggal.getText(),
                        txtJam.getText(),
                        txtTopik.getText(),
                        txtIsi.getText(),
                        txtGambar.getText()
                );
                // Refresh data tabel
                objekKu.tampilDataAgenda(tableAgenda, "select * from agenda");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID Agenda belum diisi (Pilih data dari tabel)");
                txtId.requestFocus();
            } else {
                // Menggunakan hapusAgenda02 (PreparedStatement)
                objekKu.hapusAgenda02(txtId.getText());
                
                // Kosongkan field
                txtId.setText(null);
                txtTanggal.setText(null);
                txtJam.setText(null);
                txtTopik.setText(null);
                txtIsi.setText(null);
                txtGambar.setText(null);
                
                // Refresh data tabel
                objekKu.tampilDataAgenda(tableAgenda, "select * from agenda");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        String cari = "select * from agenda where Topik like '%" + txtCari.getText() + "%'"
                + " OR Isi like '%" + txtCari.getText() + "%' ";
        objekKu.tampilDataAgenda(tableAgenda, cari);
    }//GEN-LAST:event_txtCariKeyPressed

