    // PERBAIKAN: FUNGSI BARU UNTUK MENG-HANDLE KLIK PADA TABEL
    // =================================================================
    private void tableAgendaMouseClicked(java.awt.event.MouseEvent evt) {
        // Mengisi form saat data di tabel di-klik
        int baris = tableAgenda.rowAtPoint(evt.getPoint());
        String id = tableAgenda.getValueAt(baris, 0).toString();
        String tanggal = tableAgenda.getValueAt(baris, 1).toString();
        String jam = tableAgenda.getValueAt(baris, 2).toString();
        String topik = tableAgenda.getValueAt(baris, 3).toString();
        String isi = tableAgenda.getValueAt(baris, 4).toString();
        
        // Menangani jika kolom 'gambar' kosong (null)
        String gambar = "";
        if (tableAgenda.getValueAt(baris, 5) != null) {
            gambar = tableAgenda.getValueAt(baris, 5).toString();
        }
        
        txtId.setText(id);
        txtTanggal.setText(tanggal);
        txtJam.setText(jam);
        txtTopik.setText(topik);
        txtIsi.setText(isi);
        txtGambar.setText(gambar);
    }
